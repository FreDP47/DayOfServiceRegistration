                                    <dt style="margin-top: 2%">Q. What activities will happen on that day?</dt>
                                    <dd><strong>Ans.</strong> We engage with children/elderly through activities like storytelling, craft and fun games. The Coordinator will inform you about the activities that will be conducted that day. However, you may suggest activities to the Coordinator before the event.</dd>
