                                    <dd><strong>Ans.</strong> Bakul has made arrangements for refreshments. Participants are not allowed to distribute any food item or gifts in the slum/shelter home without prior consultation with the Bakul Coordinator.  If you wish to contribute financially for the Day of Service or to Bakul, you can donate at Bakul Library – 16, Satyanagar or electronically.</dd>
