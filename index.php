                                    <dt style="margin-top: 2%">Q. Can I take my camera along?</dt>
                                    <dd><strong>Ans.</strong> A Photographer will be appointed by Bakul. Photography during the activity is strictly prohibited unless the Bakul Coordinator asks you to. However, participants are allowed to take photographs after the event. If you wish to be the designated photographer at your location, please contact your Coordinator.</dd>
                                    <dt style="margin-top: 2%">Q. What do I need to bring along with me for the event?</dt>
                                    <dd><strong>Ans.</strong> You may bring your essentials along with you. But do avoid bringing anything for the children. Also, it would be great if you can bring water for yourself and a lot of energy.</dd>
                                    <dt style="margin-top: 2%">Q. Can I bring more people along with me?</dt>
                                    <dd><strong>Ans.</strong> You may but with prior consultation with the Coordinator.</dd>
                                    <dt style="margin-top: 2%">Q. Is there any dress code?</dt>
                                    <dd><strong>Ans.</strong> No. However, please do keep in mind that we may have outdoor activities.</dd>
                                    <dt style="margin-top: 2%">Q. Will I get a certificate?</dt>
                                    <dd><strong>Ans.</strong> Yes, if required. But, only those participants who fill up the post event feedback form will be given certificates in a digital format.</dd>
